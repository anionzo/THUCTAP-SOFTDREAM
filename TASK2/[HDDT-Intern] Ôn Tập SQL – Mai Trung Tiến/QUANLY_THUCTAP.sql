--- Câu 11: Cho biết mã số, họ tên, tên khoa của các giảng viên hướng dẫn từ 3 sinh viên trở lên
SELECT GV.MAGV, GV.HOTENGV, K.MAKHOA  FROM HUONGDAN HD
JOIN GIANGVIEN GV ON GV.MAGV = HD.MAGV
JOIN SINHVIEN SV ON SV.MASV = HD.MASV
JOIN KHOA K ON GV.MAKHOA = K.MAKHOA
GROUP BY  GV.MAGV, GV.HOTENGV, K.MAKHOA
HAVING COUNT(DISTINCT SV.MASV) >= 3

