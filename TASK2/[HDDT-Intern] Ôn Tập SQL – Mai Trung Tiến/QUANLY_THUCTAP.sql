-- TẠO BẢNG Khoa(makhoa char(10), tenkhoa char(30), dienthoai char(10))
CREATE TABLE KHOA(
	MAKHOA char(10) PRIMARY KEY,
	TENKHOA nvarchar(70),
	DIENTHOAI CHAR(10)
)
GO

--GiangVien(magv int, hotengv char(30), luong decimal(5,2), makhoa char(10))
CREATE TABLE GIANGVIEN(
	MAGV INT PRIMARY KEY,
	HOTENGV nvarchar(70),
	LUONG DECIMAL(12,2),
	MAKHOA CHAR(10)
)
GO

--SinhVien(masv int, hotensv char(30), makhoa char(10), namsinh int, quequan char(30))
CREATE TABLE SINHVIEN(
	MASV INT PRIMARY KEY,
	HOTENSV nvarchar(70),
	MAKHOA CHAR(10),
	NAMSINH INT,
	QUEQUAN nvarchar(70)
)
GO
--DeTai(madt char(10), tendt char(30), kinhphi int, NoiThucTap char(30))
CREATE TABLE DETAI(
	MADT CHAR(10) PRIMARY KEY,
	TENDT nvarchar(70),
	KINHPHI INT,
	NOITHUCTAP nvarchar(70)
)

--HuongDan(masv int, madt char(10), magv int, ketqua decimal(5,2))
CREATE TABLE HUONGDAN(
	MASV INT ,
	MADT CHAR(10),
	MAGV INT,
	KETQUA DECIMAL(5,2),
	PRIMARY KEY(MASV,MADT,MAGV)

)
GO

