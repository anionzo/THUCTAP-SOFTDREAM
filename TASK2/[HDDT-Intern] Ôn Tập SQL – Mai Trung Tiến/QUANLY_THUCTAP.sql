--- CÂU 2: Đưa ra thông tin gồm mã số, họ tên và tên khoa của tất cả các giảng viên của khoa  'DIA LY va QLTN'
SELECT GV.MAGV, GV.HOTENGV, K.TENKHOA FROM GIANGVIEN GV, KHOA K 
WHERE GV.MAKHOA = K.MAKHOA AND K.TENKHOA LIKE 'DIA LY va QLTN'
--- CÂU 3: Cho biết số sinh viên của khoa 'CONG NGHE SINH HOC'
SELECT COUNT(*) AS 'SOSINHVIEN' FROM SINHVIEN SV, KHOA K
WHERE SV.MAKHOA = K.MAKHOA AND K.TENKHOA LIKE 'CONG NGHE SINH HOC'
--- CÂU 4: Đưa ra danh sách gồm mã số, họ tên và tuổi của các sinh viên khoa 'TOAN'
SELECT SV.MASV, SV.HOTENSV, (YEAR(GETDATE()) - SV.NAMSINH ) AS TUOI 
FROM SINHVIEN SV
JOIN KHOA K ON SV.MAKHOA = K.MAKHOA 
WHERE K.TENKHOA LIKE 'TOAN'
--- CÂU 5: Cho biết số giảng viên của khoa 'CONG NGHE SINH HOC'
SELECT COUNT(*) as SoLuongGV FROM GIANGVIEN GV
